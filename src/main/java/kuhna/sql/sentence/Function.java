package kuhna.sql.sentence;

import java.io.*;

/**
 * DB Function 형태의 문장을 만들기 위한 객체<BR>
 *
 * @version 0.1, 2011/10/21, initial version by A.J.Kuhn
 *
 * @author <a href="http://www.ajkuhn.com" target="_blank">A.J.Kuhn</a>
 */
public class Function extends Object implements Serializable {

  protected String _functionString;

  public Function() {
    super();
  }
  
  public Function(String functionString) {
    super();

    _functionString = functionString;
  }

  public void setFunctionString(String functionString) {
    _functionString = functionString;
  }
  
  public String toString() {
    return _functionString;
  }
}

